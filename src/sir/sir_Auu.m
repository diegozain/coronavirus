function Auu = seir_Auu(u,p,nP,it)
% -----------------------------------------------------------------------------
% diego domenzain
% spring 2020 while at Colorado School of Mines
% ------------------------------------------------------------------------------
% inversion of the SIR model using the adjoint method.
% this function computes the derivative of A wrt u times u.
% ------------------------------------------------------------------------------
b = p(1);
% ------------------------------------------------------------------------------
Auu      =  zeros(3,3);
% ------------------------------------------------------------------------------
Auu(1,3) = -(b/nP)*u(it,1);
Auu(2,3) =  (b/nP)*u(it,1);
% ------------------------------------------------------------------------------
end